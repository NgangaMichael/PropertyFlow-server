import PDFDocument from 'pdfkit';

export const generateLeasePDF = (tenant, property) => {
  return new Promise((resolve, reject) => {
    const doc = new PDFDocument({ size: 'A4', margin: 50 });
    let buffers = [];
    doc.on('data', buffers.push.bind(buffers));
    doc.on('end', () => resolve(Buffer.concat(buffers)));

    // --- Header ---
    doc.fillColor('#4f46e5').fontSize(20).text('GRANDNUM PROPERTIES', { align: 'right' });
    doc.fillColor('#444').fontSize(10).text('Management & Real Estate', { align: 'right' });
    doc.text('Nairobi, Kenya', { align: 'right' });
    doc.moveDown();

    // --- Title ---
    doc.fillColor('#333').fontSize(18).text('RESIDENTIAL LEASE AGREEMENT', { underline: true });
    doc.moveDown();

    // --- Section 1: Parties ---
    doc.fontSize(12).fillColor('#4f46e5').text('1. THE PARTIES');
    doc.fillColor('#333').fontSize(10).text(`Tenant Name: ${tenant.firstname} ${tenant.lastname}`);
    doc.text(`ID / KRA PIN: ${tenant.idnumber} / ${tenant.kraPin || 'N/A'}`);
    doc.text(`Occupation: ${tenant.occupation || 'N/A'}`);
    doc.moveDown();

    // --- Section 2: Premises ---
    doc.fontSize(12).fillColor('#4f46e5').text('2. THE PREMISES');
    doc.fillColor('#333').fontSize(10).text(`Property Name: ${property?.propertyname || 'As Assigned'}`);
    doc.text(`Location: ${property?.location || 'Nairobi'}`);
    doc.moveDown();

    // --- Highlight Box (Lease Term) ---
    doc.rect(50, doc.y, 500, 50).fill('#f3f4f6');
    doc.fillColor('#4f46e5').text('LEASE DURATION', 60, doc.y + 10, { stroke: true });
    doc.fillColor('#333').text(`Commencement: ${tenant.leasestarts} | Termination: ${tenant.leaseends}`, 60, doc.y + 15);
    doc.moveDown(4);

    // --- Section 3: Terms ---
    doc.fontSize(12).fillColor('#4f46e5').text('3. CORE TERMS & CONDITIONS');
    doc.fillColor('#333').fontSize(9).text('• RENT: Due on the 1st of every month.', { bulletRadius: 2 });
    doc.text('• MAINTENANCE: Tenant shall maintain the interior in a clean state.');
    doc.text('• TERMINATION: One full month notice is required before vacating.');
    
    // --- Footer ---
    doc.fontSize(8).fillColor('#999').text(
      `Generated by Grandnum System on ${new Date().toLocaleDateString()}`, 
      50, 780, { align: 'center' }
    );

    doc.end();
  });
};

